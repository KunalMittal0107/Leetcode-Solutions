class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
     // your code goes here
     if(head==NULL || head->next==NULL){
         return head;
     }
     unordered_map<int,int>mp;
     Node *left=head;
     Node *right=head->next;
     mp[left->data]++;
     while(left!=NULL && right!=NULL){
         if(mp.find(right->data)!=mp.end()){
             right=right->next;
         }else{
             mp[right->data]++;
             left->next=right;
             left=right;
             right=right->next;
         }
     }
     left->next=right;
     return head;
    }
};


